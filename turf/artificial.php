        <div class="row">
            <div class="col-12">
              <h2>Our Installation Process</h2>
              <div class="m-4">
                <h4>Preparation</h4>
                <ul class="font-italic">
                <li>Spraying of any existing weeds with a chemical such as Round Up.</li>
                <li>After a 2 week period these dead weeds and some underlying soil will be removed with a bobcat for the larger areas and manually for the smaller areas.</li>
                <li>All rubbish will be placed in either a skip bin or onsite if requested.</li>
                <li>Depending on the area, between 50mm-100mm of fine crusher dust will be brought in, spread, screeded and compacted to required levels.</li>
                <li>A 100mm concrete glue strip will be installed to the area by trenching around the perimeter of works and filling with concrete mix. This is to have a point of contract to secure the synthetic turf down using adhesive to avoid the use of pins (if applicable).</li>
              </ul>

              <h4>Turf Laying</h4>
                <ul class="font-italic">
                <li>Turf will be installed a week after the concrete glue strip is installed to allow the appropriate curing time.</li>
                <li>The Synthetic turf will be cut to fit the area and any joins will be joined using a special fabric tape and outdoor adhesive.</li>
                <li>Synthetic turf around the edges will be secured to the concrete glue strip (if applicable)</li>
                <li>If Synthetic turf is to be installed directly onto concrete or tiles, it will be secured directly to the base using outdoor adhesive.</li>
                
              </ul>
               <h4>Post Laying</h4>
                <ul class="font-italic">
                <li>After laying has been completed our installers will spread kiln dried sand as per the manufacturerâ€™s product warranty. The amount required will depend on the variety chosen and pile height. This should be carried out whilst the surface is dry to allow the sand to filter into the pile.</li>
                <li>Using a power broom or hard bristled broom, spread the sand through to pile.</li>
                <li>Scrap bits of turf will be disposed of and the area will be swept clean.</li>
              </ul>
              </div>


