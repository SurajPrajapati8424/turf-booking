The mixture is then sprayed over the prepared ground using a powerful truck mounted pump which has the capability of spraying over large areas. For those more difficult to each areas, a retractable hose can be used

Hydroseeding is widely used in the re-vegetation of disturbed areas within the civil engineering, mining and land development industries, especially on slopes where there is a significant risk of erosion occurring during the period of initial plant establishment.</p>
