          <div class="p-3">
          <p>Thatch is a layer of lay of both living and dead organic material which lies in between the grass and the soil, which is essential to protect your lawn from wear and damage. It becomes problematic when this layer becomes too dense and begins to affect your lawn’s health and recovery.

Due to the restriction of water, air and nutrients your grass will have to root in the thatch layer itself. This will cause your lawn to become vulnerable to drought.

For further details, please call 1300 228 873 and discuss the benefits of Dethatching.</p>
